#include <iostream>
using namespace std;

int main() {
  cout << "\n\x1b[30mThis is Black Text\n";
  cout << "\n\x1b[31mThis is Red Text\n";
  cout << "\n\x1b[32mThis is Green Text\n";
  cout << "\n\x1b[33mThis is Yellow Text\n";
}
